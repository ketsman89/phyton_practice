a = "Не знаю, как там в Лондоне, я не была. Может, там собака — друг человека. А у нас управдом — друг человека!"
print("Количество символов: ",len(a))
print("Развёрнутая строка: ",a[::-1])
print("Все слова с заглавной буквы: ",a.title())
print("Все заглавные буквы: ",a.upper())
print("Вся строка прописными буквами: ",a.lower())
print("Количество вхождений 'зн':",a.count("'зн'"),", ""ам:",a.count("ам"),", ""о:",a.count("о"))
print("Замена слова:",a.replace("управдом", "ЖЭС").replace("собака", "кот"))
print("Смена регистра:",a.swapcase())
b = a.split()
b.reverse()
b2 = ' '.join(b)
print("Обратный порядок строки:",b2)
print("Исходная строка:",a)